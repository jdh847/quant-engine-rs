        assert_eq!(Language::from_tag("zh"), Language::Zh);
        assert_eq!(Language::from_tag("zh-CN"), Language::Zh);
        assert_eq!(Language::from_tag("ja"), Language::Ja);
        assert_eq!(Language::from_tag("jp"), Language::Ja);
