#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Language {
    En,
    Zh,
    Ja,
}

impl Language {
    pub fn from_tag(tag: &str) -> Self {
        match tag.to_ascii_lowercase().as_str() {
            "zh" | "zh-cn" | "zh-hans" => Self::Zh,
            "ja" | "jp" | "ja-jp" => Self::Ja,
            _ => Self::En,
        }
    }

    pub fn html_lang(self) -> &'static str {
        match self {
            Language::En => "en",
            Language::Zh => "zh-CN",
            Language::Ja => "ja",
        }
    }
}

